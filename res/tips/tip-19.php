     <h5 class = "inner-h5">Troubleshooting Tips Bluetooth is not visible</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Confrim if Bluetooth is Discoverable</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
               <li>
              		Open your <b>Settings</b> app Settings and then 
              </li>
              <li>
        	      To enable Discovery mode tap <b>Allow other Bluetooth devices to find my phone</b>.<br>
           		   Note Enabled when a check mark is present.
              </li>

          </ul>
      </p>
  </li>
  <li>
        	<h5 class = "tip-h5" >Clear Bluetooth Cache</h5>
        	<p>
        		    <ul style = "list-style-type: circle;">
        		       <li>
        		      		Open your <b>Settings</b> app Settings and then 
        		      </li>
        		      <li>
  							Go to <b>Application Manager	</b>	      
        		      </li>
        		      <li>
        		      	Ensure all System Apps are shown by either swiping left or right or tapping More menu in the top right and choosing <b>Show system apps</b>
        		      </li>
        		      <li>
        		      	Scroll down and select Bluetooth Share. Then, tap <b>Force Stop</b> and then tap on <b>Storage and then Clear Application Data</b>
        		      </li>
        		      <li>
        		      	Go back and do the same thing for the system apps called <b>Bluetooth Test</b> and <b>Bluetooth MIDI Services</b> (if shown) (Force Stop and then tap on Storage and then Clear Application Data)
        		      </li>
        		      <li>
        		      	Toggle Bluetooth off and then back on<br>
        		      	Once completed, restart you phone
        		      </li>
        		  </ul>
        		

        	</p>
  	

  	
  	
  
  	
  </li>
</ul>
