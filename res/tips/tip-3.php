<div class = "tip" id = "tip-3">
<h5 class = "inner-h5">Troubleshooting Tips if download does not start</h5>
    <ul>
        <li >
            <h5 class = "tip-h5" >Restart Device</h5>
            <p>
            <ul style = "list-style-type: circle;">
              <li >
                 To restart your device, press and hold the power button for a few seconds. 
                     Then, on your screen, tap <b>Restart</b>
              </li>
              </ul>
            </p>
        </li>
        <li>
            <h5 class = "tip-h5" > If restarting doesn't work, switch between Wi-Fi and mobile data:</h5>
            <ul style = "list-style-type: circle;">
                <li>
                        Open your Settings app Settings and then <b>Wireless & networks</b> or 
                        <b>Connections</b>. Depending on your device, these options may be different. 
                </li>
                <li>
                        Turn Wi-Fi off and mobile data on, and check to see if there's a difference.

                </li>
                <li>
                      If not, turn mobile data off and Wi-Fi on and see if that works.

                </li>
            </ul>
         </li>
         <li>
         <h5 class = "tip-h5" > If mobile data still has problems</h5>
         <ul style = "list-style-type: circle;">
                <li>
                    Open your <b>Settings</b> app Settings and then <b>Wireless and Networks</b> or 
                    <b>Connections</b> and then <b>Mobile data</b> or <b>Cellular data</b>. 
                    On some devices, you may need to select <b>Data usage</b> before you see this. 
                </li>
                <li>
                    Turn mobile data on. If it's already on, turn it off and on again.
                </li>
                <li>
                    Check that there is a data indicator (for example, 2G, 3G, 4G, H)
                     next to the signal strength bars at the top of the screen Signal.
                    
                </li>
                <li>
                Note that sometimes this will not display if you have an active Wi-Fi connection.
                 If this is the case, turn Wi-Fi off and check back.
    
                </li>
                <li>
                If you don't see a signal data indicator, you may be in an area without coverage. If you can, move to a different area (for example, go outside) and check your signal again.

                </li>
                
        </ul>
     </li>
    </ul>
</div>