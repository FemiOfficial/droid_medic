    <h5 class = "inner-h5">Troubleshooting tips for unresponsive screen</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Basic things to try out first</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li>
              	Make sure your hands are clean and dry.
              </li>
              <li>
              	Clean the screen with a soft, slightly damp, lint-free cloth.
              </li>
              <li>
              	Unplug your device.
              </li>
              <li>
              	Restart your device. If you can't restart it, you can force restart your device
              </li>
          </ul>
      </p>
  </li>
  <li>
     <h5 class = "tip-h5" >Remove Memory Card and Sim Card</h5>
 	 <p>
     <ul style = "list-style-type: circle;">
       <li>
       		Force device to switch off by long pressing the power button
       </li>
       <li>
       		Remove Sim card and memory card
       </li>
       <li>
       		Reboot device and see if that solves the problem
       </li>
      </ul>	
  </li>
  <li>
     <h5 class = "tip-h5" >Put device in safe mode to disable third party</h5>
     <ul>
     	<li>
     		Power off your Android device.
     	</li>
     	<li>
     		Long press power button to reboot the device
     	</li>
     	<li>
     		When you see the device brand logo, release power button and hold down Volume Down button
     	</li>
     	<li>
     		Release Volume Down button when the device boots up with a Safe mode indicator on the bottom left corner.
     	</li>
     </ul>
  </li>
  <li>
     <h5 class = "tip-h5" >Factory Reset Android Device in Recovery Mode</h5>
 		 <p>
 		 <h5 class = "text-danger">Remember to take a back-up of all your data and contents on the cloud or
 		  an external memory device before adopting this method.</h5>
 		 <ul style = "list-style-type: circle;">
 		         <li>Open your device's <b>Settings</b> app and tap <b>Storage</b> . </li>
 		         <li>Now tap <b>Backup and Reset</b></li>
 		         <li>Select <b>Factory data reset</b> and then <b>Reset Device</b></li>
 		         <li>Tap on <b>ERASE EVERYTHING</b> as shown below to <b>Factory Reset your device</b>.</li>

 		</ul>
 		 </p>
  		
  </li>
</ul>
</div>