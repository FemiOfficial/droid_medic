<div class = "tip" id = "tip-20">
    <h5 class = "inner-h5">Troubleshooting tips device USB connection to PC</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Restart both devices if unable to connect</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li>
              	Don't panic, restart the phone and the PC. Should do the job and if not:
              </li>
              
             
          	</ul>
      </p>
  </li>
  <li>
      <h5 class = "tip-h5" >Update both devices</h5>
      	 <p>
          <ul style = "list-style-type: circle;">
          	<b>Update your PC and try the connection again</b>
            <li>
            	Open <b>Settings</b> on your PC and check for update (requires internet connection)
            </li>
            <LI>
				If there is any updates, install them
            </LI>
            <li>
            	Restart the PC and try USB connection to phone again, no change? Then:<br>
            </li>
            <b>Update device if any is required</b>
            <li>Open device's <b>Settings</b> App and then tap <b>System</b></li>
            <li>Tap <b>App Update</b> to check for Update</li>
            <li>Install all updates found</li>
            <li>Slot in USB to PC and try again</li>
        </ul>
    </p>
  </li>
  <li>
  	  <h5 class = "tip-h5" > Use USB Debugging</h5>
  	  	 <p>
  	      <ul style = "list-style-type: circle;">
  	        <li>
  	        	Open device's <b>Settings</b> App 
  	        </li>
  	        <LI>
  	        	Tap <b>Developer Option</b> and tap<b>USB Debugging</b>
  	        </LI>
  	        <li>
  	        	When you see a pop-up warning click <b>OK</b>
  	        </li>
  	        <LI>Try out USB connection again</LI>
  	      
  	    </ul>
  	</p>
  	
  </li>
</ul>
    
</div>
