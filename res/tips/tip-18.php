    <h5 class = "inner-h5">Troubleshooting Tips Bluetooth unable to connect to other devices</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Switch Bluetooth On and Off (Probably tried this)</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li>
              		Open your <b>Settings</b> app Settings and then 
              </li>
              <li>
              		  Tap <b>Connected devices</b> and then <b>Connection preferences</b> and then <b>Bluetooth</b>. If you don't see <b>Connection preferences</b>, go to Step 3.
              </li>
              <li>
              	Turn Bluetooth on or off.
              </li>

             </ul>
         </p>
     </li>
     <li>
     			<h5 class = "tip-h5" >Charge your device and move closer</h5>
     			 <p>
     		    <ul style = "list-style-type: circle;">
     		      <li>
     		      		Proximity is sometimes an issue with bluetooth connection, make sure you are not too far from neighbouring device (max 3m).

     		      </li>
     		      <li>
     		      		It also helps to fully charge your device and try connecting again
     		      	
     		      </li>
     		     </ul>
     </li>
 </ul>
