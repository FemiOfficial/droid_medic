    <h5 class = "inner-h5">Troubleshooting tips for Account Sync Issues</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Try manually syncing your Google Account</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
               <li>
             	  Open your device’s <b>Settings</b> app.		
               </li>
                <li>
             	 Tap <b>Accounts</b>.	
               </li>
                <li>
              		 Tap the account.
             	  	
               </li>
                <li>
               Tap <b>Account sync</b>.
             	 	
               </li>
                <li>
               Tap <b>More</b> and then <b>Sync now</b>.
               </li>
               <li>
               	<b>This should solve the problem, if not try the advanced steps</b>.
               </li>       
           </ul>
       </p>
   </li>
   <li>
      <h5 class = "tip-h5" >Troubleshooting the account sync issues with advanced steps</h5>
   		<ul style = "list-style-type: circle;">
   				<h5><b>Option 1: Change date & time settings</b></h5>
               You can try to force sync your account by manually resetting your time settings.

               <li>
             	  Open your device’s <b>Settings</b> app.		
               </li>
               <li>
               Tap <b>System</b> and then <b>Date & time</b>.               	
               </li>
               <li>
               Turn off Automatic date & time and Automatic time zone.             	
               </li>
               <li>
               Manually change the date and time so that both are wrong.
               	
               </li>
               <li>
               Tap <b>Home</b> button.
               	
               </li>
               <li>
               Open your device's <b>Settings</b> app and then <b>System</b> and then <b>Date & time</b> again.
               	
               </li>
               <li>
               Manually change the date and time so that both are right again.
               	
               </li>
               <li>
               Turn on Automatic date & time and Automatic time zone.	
               </li>
   				<h5><b>Option 2: Force sync using Phone app (phones only)</b></h5>
   				You can try to force sync your account on your phone using the Phone app.
   				<li>
   				Open your phone's <b>Phone</b> app.  					
   				</li>
   				<li>
   				Tap the <b>KeyPad</b>.				
   				</li>
   				<li>
   				Dial <span class="text-danger">*#*# CHECKIN #*#* (*#*#2432546#*#*)</span>.
   					
   				</li>
   				<li>
   					You'll see a notification that says <b>Checkin succeeded</b> . This message shows your phone was able to sync. If you don't see a notification right away, tap the green Phone <b>Answer call icon</b>
   				</li>



   				



        </ul>
   </li>
</ul>
