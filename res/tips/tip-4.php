      <h5 class = "inner-h5">Troubleshooting tips for inablity to send SMS messages</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Clear App Cache and try with Airplane Mode</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li >
                Open your device's Settings app <b>Settings</b>.
              </li>
              <li>
					Tap <b>Apps Manager</b>.       	
              </li>
              <li>
					Scroll down and tap <b>Messages</b> app.           	
              </li>
              <li>
					Tap <b>Storage</b> and then <b>Clear Cache</b>.           	
					          	
              </li>
              <li>
              		Choose Storage
              </li>
                <li>
					Next, tap Clear cache.
              </li>
              </ul>
            </p>
   </li>
   <li>
       	<h5 class = "tip-h5" >Reset SMS settings to default values</h5>
	    	 <p>
	        <ul style = "list-style-type: circle;">
	          <li >
	            Open your device's <b>Messages</b> app.
	          </li>
	          <li>
					Tap <b>Settings</b>.       	
	          </li>
	          <li>
					Reset all settings to factory values .           	
	          </li>
	          <li>
					Restart android device         	
					          	
	          </li>
	         
	          </ul>
	        </p>
   </li>
  <li>
      	<h5 class = "tip-h5" >Change the Mobile Message Center Number</h5>
	    	 <p>
	        <ul style = "list-style-type: circle;">
	          <li >
	            Open your device's <b>Messages</b> app.
	          </li>
	          <li>
					Tap on the <b>3-line (or 3-dot menu)</b>.       	
	          </li>
	          <li>
					Choose Message Center and change the number.           	
	          </li>
	         
	          </ul>
	        </p>
  </li>
    <li>
        	<h5 class = "tip-h5" >Reset Mobile Network Settings</h5>
  	    	 <p>
  	        <ul style = "list-style-type: circle;">
  	          <li >
  	            Open your device's <b>Settings</b> app.
  	          </li>
  	          <li>
  					Tap <b>System</b>.       	
  	          </li>
  	          <li>
  					Choose <b>Reset Options</b>.           	
  	          </li>
  	          <li>
  	          	Tap Reset Wi-Fi, mobile & Bluetooth.
  	          </li>
  	          <li>
  	          	Confirm selection.
  	          </li>
  	         
  	          </ul>
  	        </p>
    </li>
    <li>
    	      	<h5 class = "tip-h5" >Check if the SIM card is properly fixed</h5>
    	
    </li>
    <li>
   		<h5 class = "tip-h5" >Try another Messaging App, if none of the stated tips does the job</h5>
    	
    </li>
</ul>
