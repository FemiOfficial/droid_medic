<div class = "tip" id = "tip-21">
    <h5 class = "inner-h5">Troubleshooting tips if bluetooth does not turn on</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Restart the Android Device and try again</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
               <li>
               	Long Press the Power Button
               </li>
                <li>
               	Tap <b>Restart Device</b> or <b>Reboot</b>
               </li>
                <li>
              		Open your <b>Settings</b> app and then 
              </li>
              <li>
              		  Tap <b>Connected devices</b> and then <b>Connection preferences</b> and then <b>Bluetooth</b>. If you don't see <b>Connection preferences</b>, go to Step 3.
              </li>
              <li>
              	Turn Bluetooth on or off.
              </li>
           </ul>
       </p>
   </li>
        <li >
            <h5 class = "tip-h5" >Last Option: Factory reset your device.</h5>
            <p>
            <h5 class = "text-danger">Remember to take a back-up of all your data and contents on the cloud or
             an external memory device before adopting this method.</h5>
            <ul style = "list-style-type: circle;">
                    <li>Open your device's <b>Settings</b> app and tap <b>Storage</b> . </li>
                    <li>Now tap <b>Backup and Reset</b></li>
                    <li>Select <b>Factory data reset</b> and then <b>Reset Device</b></li>
                    <li>Tap on <b>ERASE EVERYTHING</b> as shown below to <b>Factory Reset your device</b>.</li>
                    <li>Try to turn on Bluetooth again after Hard Reset is completed</li>


           </ul>
            </p>
        </li>
</ul>
</div>