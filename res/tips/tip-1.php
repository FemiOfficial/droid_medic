    <h4>It could be from Apps installed or the Device</h2>
    <h5 class = "inner-h5">Check the Device</h5>
    <ul>
        <li> To restart your device, press and hold the power button for a few seconds. 
        Then, on your screen, tap <b>Restart</b></li>
    </ul>
    <h5 class = "inner-h5">Checking Installed Apps, if rebooting the device does not solve the problem</h5>
    <ul>
        <li >
            <h5 class = "tip-h5" >Restrict Apps that use the most battery</h5>
            <p>
                <ul style = "list-style-type: circle;">
                <h5 class = "inner-h5">Restrict Apps with high background battery use</h5>
                    <li>Open your device's <b>Settings</b> app. </li>
                    <li>Tap <b>Battery</b>.</li>
                    <li>If you see a card with advice to restrict an app, tap the card and then <b>Restrict</b>. <br>
                        Restrict apps with high overall battery use.
                    </li>
                 
                <h5 class = "inner-h5">Restrict apps with high overall battery use.</h5>
                <li>Open your device's <b>Settings</b> app.</li>
                <li>Tap Battery and then <b>More</b> and then <b>Battery usage.</b></li>
                <li>Under <b>Battery usage since full charge,</b> see a list of apps with the percentage of your battery used.
                <br>
                Tip: To see services as well as apps, tap More More and then Show full device usage.<br>
                To see or change how a listed app uses battery, tap it.​
                </li>
                <li>For some apps, you can turn on <b>Background restriction.</b><br>
                    For all apps, we recommend keeping <b>Battery optimization</b> on. 
                </li>

                </ul>
            </p>
        </li>
        <li >
            <h5 class = "tip-h5" >Force Problem Apps to Stop</h5>
            <ul style = "list-style-type: circle;">
                <h5 class = "inner-h5">To Force stop an App:</h5>          
                    <li>Open your device's <b>Settings</b> app. </li>
                    <li>Tap <b>Apps & notifications.</b></li>
                    <li>Pick the app from the list.</li>
                    <li>To see more apps, tap See all apps.</li>
                    <li>If you don't see the list, tap <b>App info.</b> </li>
                    <h5 class = "inner-h5">To start stopped app, same step and if problem does not change uninstall it</h5>         
            </ul>

        </li>
        <li> 
            <h5 class = "tip-h5">Uninstall Problem Apps</h5>
            <ul style = "list-style-type: circle;">
                <h5 class = "inner-h5">If an app still uses too much battery after you’ve force stopped and restarted it, you can uninstall it with this few steps.</h5>          
                    <li>Touch and hold the app that you want to uninstall.</li>
                    <li>To see your options, start dragging the app.</li>
                    <li>Drag the app to <b>Uninstall</b> at the top of the screen. If you don't see <b>Uninstall</b>, you can't uninstall the app.</li>
                    <li>Lift your finger.</li>
               </ul>
        
        </li>

    </ul>

