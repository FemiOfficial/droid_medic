<h5 class = "inner-h5">Troubleshooting Tips to help you speed up your device once again.</h5>
    <ul>
        <li >
            <h5 class = "tip-h5" >Clear Cache on the Device to free up storage space</h5>
            <p>
                <ul style = "list-style-type: circle;">
                    <li>Open your device's <b>Settings</b> app and tap <b>Storage</b> . </li>
                    <li>Tap <b>Cached Data</b> then <b>OK</b> to clear all unwanted Cache on the device . </li>

                </ul>
            </p>
        </li>
        <li >
            <h5 class = "tip-h5" >Uninstall unwanted heavy apps</h5>
            <p>
            <ul style = "list-style-type: circle;">
                    <li>Touch and hold the app that you want to uninstall.</li>
                    <li>To see your options, start dragging the app.</li>
                    <li>Drag the app to <b>Uninstall</b> at the top of the screen. If you don't see <b>Uninstall</b>, you can't uninstall the app.</li>
                    <li>Lift your finger.</li>
               </ul>
            </p>
        </li>
        <li >
            <h5 class = "tip-h5" >Disable Unwanted Widgets</h5>
            <p>
            <ul style = "list-style-type: circle;">
                    <li>Long press on the Widget.</li>
                    <li>Now drag it to the <b>X</b> or <b>Remove</b> icon to delete it.</li>
           </ul>
            </p>
        </li>
        <li >
            <h5 class = "tip-h5" > Factory resets your device.</h5>
            <p>
            <h5 class = "text-danger">Remember to take a back-up of all your data and contents on the cloud or
             an external memory device before adopting this method.</h5>
            <ul style = "list-style-type: circle;">
                    <li>Open your device's <b>Settings</b> app and tap <b>Storage</b> . </li>
                    <li>Now tap <b>Backup and Reset</b></li>
                    <li>Select <b>Factory data reset</b> and then <b>Reset Device</b></li>
                    <li>Tap on <b>ERASE EVERYTHING</b> as shown below to <b>Factory Reset your device</b>.</li>

           </ul>
            </p>
        </li>
    </ul>
    
</div>