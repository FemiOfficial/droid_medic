<div class = "tip" id = "tip-9">
    <h5 class = "inner-h5">Troubleshooting tips for cracked speaker</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Finding Out the Actual Problem</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li>
              	Speaker problems are sometimes mistaken for microphone problems
              </li>
              <li>
              	<b>Speaker Problem</b> If you are experiencing poor sound quality or cannot hear a single thing.
              </li>
              <li>
              	<b>Microphone Problem</b> If you can hear on the phone but other cannot hear you
              </li>
          	</ul>
      </p>
  </li>
  <li>
      <h5 class = "tip-h5" > Troubleshooting Speaker Problem</h5>
      	 <p>
          <ul style = "list-style-type: circle;">
            <li>
            	Note: Mobile Phone has two speakers (Internal and External)
            </li>
            <LI>
            	Internal Speakers for Calls: Cracking or fading of this could be network issue during the call
            </LI>
            <li>
            	External Speaker for much louder sound: Cracking sound from this definitely an hardware problem and you need to see an authorized Android technician to fix it.
            </li>
        </ul>
    </p>
  	
  </li>
  <li>
  	  <h5 class = "tip-h5" > Troubleshooting Microphone Problem</h5>
  	  	 <p>
  	      <ul style = "list-style-type: circle;">
  	        <li>
  	        	This could be network issues or connection carrier issues
  	        </li>
  	        <LI>
  	        	It could also be an app you recently installed
  	        </LI>
  	        <li>
  	        	Do a factory reset and make sure you back up your files before doing this
  	        </li>
  	        <li>
  	        	No solution yet? You need to take the phone to authorized Android technician to fix it.
  	        </li>
  	    </ul>
  	</p>
  	
  </li>
</ul>
    
</div>