<div class = "tip" id = "tip-16">
    <h5 class = "inner-h5">Troubleshooting tips for device not charging</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Troubleshooting the cable or charger</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li>
              	Use the charger and cable that came with your device. To check that the charger and cable are working, try them with another device.
              </li>
              <li>
              	Check that the cable is securely connected to the charger and to your device. Check that there's nothing in your device's port, like dust or lint.
              </li>
              <li>
              	Plug the charger into a wall outlet. To check that the outlet is working, plug in something else, like a lamp.
              </li>
              <li>
              	Check that no accessories, like cases or battery packs, cover your device's sensors or press its buttons.
              </li>
          	</ul>
      </p>
  </li>
  <li>
      <h5 class = "tip-h5" > Troubleshooting your battery</h5>
      	 <p>
          <ul style = "list-style-type: circle;">
          	<span class="text-danger">Within 1 minute after you've plugged in your Android device with a working cable, charger, and outlet:</span>
            <li>
            	If you see a battery icon, your device is powered off and is charging. You can restart right away.
            </li>
            <LI>
            	If you see a red light, your battery is fully discharged. If the red light is flashing, there isn't enough power to turn on. Charge your device for at least 30 minutes before restarting.
            </LI>
            <li>
            	To restart your device, press and hold the power button for a few seconds. Then, on your screen, tap <b>Restart</b>. (If you don't see "Restart," press and hold the power button for about 30 seconds, until your device restarts.)
            </li>
        </ul>
    </p>
  	
  </li>
  <li>
  	  <h5 class = "tip-h5" > Troubleshooting your screen</h5>
  	  	 <p>
  	      <ul style = "list-style-type: circle;">
  	      	<span class="text-danger">
  	      		If you don't see a battery icon or red light after you plug in your Android device, the issue could be with your screen. If the device can get calls:
  	      	</span>
  	        <li>
  	        	Press the power button for about 30 seconds.
  	        </li>
  	        <LI>
  	        	Wait about 2 minutes.
  	        </LI>
  	        <li>
  	        	Make your device ring. You can call it from another phone.
  	        </li>
  	      
  	    </ul>
  	</p>
  	
  </li>
</ul>
    
</div>
