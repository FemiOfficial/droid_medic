<h5 class = "inner-h5">Troubleshooting Tips if Google Play is stuck</h5>
    <ul>
        <li>
        	<h5 class = "tip-h5" >Double check mobile connection or WI-FI</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li >
                 First, make sure you have a strong internet connection. It’s best if you can connect to a Wi-Fi network. If you don’t have access to Wi-Fi, make sure you have a strong mobile data connection. Then try your download again. 
              </li>
              </ul>
            </p>

        </li>
        <li>
        	<h5 class = "tip-h5" >Clear the cache & data of the Play Store</h5>
        	 <p>
            <ul style = "list-style-type: circle;">
              <li >
                Open your device's Settings app <b>Settings</b>.
              </li>
              <li>
					Tap <b>Apps & notifications</b> and then See all apps.       	
              </li>
              <li>
					Scroll down and tap <b>Google Play Store</b>.           	
              </li>
              <li>
					Tap <b>Storage</b> and then <b>Clear Cache</b>.           	
					          	
              </li>
              <li>
					Next, tap Clear data.

              </li>
                <li>
					Re-open the Play Store & try your download again.
					
              </li>


          

              </ul>
            </p>
        </li>
    </ul>

</div>